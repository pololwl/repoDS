CREATE TABLE species (
    identificacion INTEGER,
    nombre_cientifico VARCHAR(255),
    reino VARCHAR(255),
    filo VARCHAR(255),
    clase VARCHAR(255),
    orden VARCHAR(255),
    familia VARCHAR(255),
    genero VARCHAR(255),
    epiteto_especifico VARCHAR(255),
    estado_taxonomico VARCHAR(255),
    estado_de_amenaza VARCHAR(255),
    coordenadas TEXT
);
